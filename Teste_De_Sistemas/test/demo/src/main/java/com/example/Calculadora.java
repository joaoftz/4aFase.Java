package com.example;

public class Calculadora {
    public Integer soma(Integer v1, Integer v2){
        return v1 + v2;
    }
    public Integer multiplicacao(Integer v3, Integer v4){
        return v3 * v4;
    }

    public Integer subtracao(Integer v5, Integer v6){
        return v5 - v6;
    }

    public Integer divisao(Integer v7, Integer v8){
        return v7 / v8;
    }
}
